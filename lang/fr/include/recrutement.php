<h1>Recrutement du Staff d'Ellàs War</h1>
<br/>
<p>Ellàs War évolue rapidement et notre staff doit régulièrement se renforcer. Si vous pensez avoir les qualités pour en faire partie, n'hésitez pas à nous contacter soit par <a href="http://forums.ellaswar.com">le forum</a> soit par <a href="http://www.ellaswar.com/nouscontacter">la page contact</a>.</p>
<br/>
<h2>Recrutement de membres pour le staff</h2>
<ul>
	<li><b>Héros :</b> Ils modérent le forum et s'assurent de son bon fonctionnement.</li>
	<li><b>Prophètes :</b> Ils sont les garants de la justice sur le jeu. Ils règlent les conflits entre les joueurs et s'occupent de l'application du règlement.</li>
</ul><br/>
<p>Afin de permettre au jeu d'évoluer, nous recrutons des développeurs. Ils ne travailleront pas directement sur le jeu mais développeront des services annexes.</p>
<h2>Recrutement de développeurs</h2>
<ul>
	<li><b>Développeur Android :</b> Développement d'une application Ellàs War</li>
	<li><b>Développeur Iphone :</b> Développement d'une application Ellàs War</li>
	<li><b>Développeur Java :</b> Développement d'une application Java Ellàs War client/serveur.</li>
	<li><b>Développeur Web :</b> Développement de services liés au jeu</li>
</ul>

Nous avons aussi pour objectif d'ouvrir l'API du site. Si vous voulez monter un projet basé sur Ellàs War, contactez-nous. Nous en discuterons et nous bâtirons ensemble un site ou une application de qualité.
